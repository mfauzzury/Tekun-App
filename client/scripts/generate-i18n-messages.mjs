function emitLocale(name, body) {
  fs.mkdirSync("src/i18n/messages", { recursive: true });
  const content = `const messages = ${JSON.stringify(body, null, 2)} as const;\n\nexport default messages;\n`;
  fs.writeFileSync(`src/i18n/messages/${name}.ts`, content, "utf8");
}

